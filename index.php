$('#regFrm').submit(function(e){
	var form = $(this);
	var name = $(this).find('input[name=name]');
	var email = $(this).find('input[name=email]');
	var phone = $(this).find('input[name=phone]');
	if(name.val() == ''){
		name.attr('value','Vui lòng nhập họ tên').css('color','red');
		return false;
	}else{
		name.attr('style','');
	}
	if(email.val() == ''){
		email.attr('value','Vui lòng nhập email').css('color','red');
		return false;
	}else{
		email.attr('style','');
	}
	if(phone.val() == ''){
		phone.attr('value','Vui lòng nhập điện thoại').css('color','red');
		return false;
	}else{
		phone.attr('style','');
	}
	$.ajax({
		url: 'http://austcare.vn/register/add',
		type: 'POST',
		data: form.serialize(),
		success: function(data){
			try{
				var obj = $.parseJSON(data);
				if(obj.message == 'success') {
					alert('Cảm ơn quý khách đã đăng kí. Chúng tôi sẽ liên hệ ngay sau khi nhận được đơn đăng kí của quý khách.');
					form.find('input').val('');
				}else 
					alert('Quá trình đăng kí bị lỗi vui lòng liên hệ Hot Line: 1900 6828. Cảm ơn quý khách');
			}catch(err){
				alert('Quá trình đăng kí bị lỗi vui lòng liên hệ Hot Line: 1900 6828. Cảm ơn quý khách');
			}
		},
		error: function(msg){
			alert('Quá trình đăng kí bị lỗi vui lòng liên hệ Hot Line: 1900 6828. Cảm ơn quý khách');
		}
	})
	e.preventDefault();
})
